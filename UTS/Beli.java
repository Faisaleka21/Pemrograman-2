    private void beliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliActionPerformed
        // TODO add your handling code here:
        
        //validasi input
        String nm=nama.getText();
        if (nm.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        String selectedItem = (String) varian.getSelectedItem();
        if("-Pilih-".equals(selectedItem)){
                JOptionPane.showMessageDialog(this, "Harap Pilih Varian", "Error", JOptionPane.ERROR_MESSAGE);
                return;
        }
        
        
        
        //total
        int total=0;
        StringBuilder x= new StringBuilder();
        
        //kerupuk
        if(kerupuk.isSelected()){
            total +=800;
            x.append("Kerupuk \t\t = Rp.800\n");
        }
        //irisan
        if(ayam.isSelected()){
            total +=2500;
            x.append("Irisan Ayam \t\t = Rp.2500\n");
        }
        //pentol
        if(pentol.isSelected()){
            total +=1000;
            x.append("Pentol Bakso \t\t = Rp.1000\n");
        }
        //sosis
        if(sosis.isSelected()){
            total +=1000;
            x.append("Sosis \t\t = Rp.1000\n");
        }
        //bakpao
        if(bakpao.isSelected()){
            total +=2000;
            x.append("Bakpao \t\t = Rp.2000\n");
        }
        //mie
        if(mie.isSelected()){
            total +=1500;
            x.append("Mie \t\t = Rp.1500\n");
        }
        //makroni
        if(makroni.isSelected()){
            total +=800;
            x.append("Makroni \t\t = Rp.800\n");
        }
        //telur
        if(telur.isSelected()){
            total +=2500;
            x.append("Telur \t\t = Rp.2500\n");
        }
        //sawi
         if(sawi.isSelected()){
            total +=800;
            x.append("Sawi \t\t = Rp.800\n");
        }
         //tahu
          if(tahu.isSelected()){
            total +=1300;
            x.append("Tahu \t\t = Rp.1300\n");
        }
         //puyuh
          if(puyuh.isSelected()){
            total +=900;
            x.append("Telur Puyuh \t\t = Rp.900\n");
        }
         //udang
          if(udang.isSelected()){
            total +=1000;
            x.append("Kerupuk Udang \t = Rp.1000\n");
        }
        
        area.setText(""+x +"-------------------------------------------------------------- +\n" + "Total \t\t = Rp."+total);
        
        //menampilkan total
        hasil.setText("Rp."+total);
        
        //tabel
        DefaultTableModel model=(DefaultTableModel)tabel.getModel();
        model.addRow(new Object[]{
            nama.getText(),
            level.getSelectedItem(),
            varian.getSelectedItem(),
            hasil.getText()
        });
        
       
    }//GEN-LAST:event_beliActionPerformed

