    private void btntotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntotalActionPerformed
        // TODO add your handling code here:
        int totalKeseluruhan = 0; // untuk menyimpan total semua harga
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
        for (int i = 0; i < tabel.getRowCount(); i++) {
        String hargaStr = tabel.getValueAt(i, 3).toString(); // Ambil nilai dari kolom ke-3 yaitu harga
        
        // Menghapus "Rp." jika ada dan konversi ke integer
        int harga = Integer.parseInt(hargaStr.replace("Rp.", ""));
        totalKeseluruhan += harga; // Menambahkan harga ke total
        }
        hasiltotal.setText("Rp."+totalKeseluruhan);
        
    }//GEN-LAST:event_btntotalActionPerformed

