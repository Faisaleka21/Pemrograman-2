    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex = tabel.getSelectedRow(); //get selected row
        if(selectedRowIndex >=0){
            kontrak.setText(tabel.getValueAt(selectedRowIndex, 0).toString());
            nama.setText(tabel.getValueAt(selectedRowIndex, 1).toString());
            telepon.setText(tabel.getValueAt(selectedRowIndex, 2).toString());
            cair.setText(tabel.getValueAt(selectedRowIndex, 3).toString());

            //combo box
            String tnr = tabel.getValueAt(selectedRowIndex, 4).toString(); // Kolom JK
            tenor.setSelectedItem(tnr); // Set nilai pada combo box
            motor.setText(tabel.getValueAt(selectedRowIndex, 5).toString());

        }
    }//GEN-LAST:event_tabelMouseClicked

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        int pilihBaris = tabel.getSelectedRow();
        if (pilihBaris != -1) {
            String nama=(String) tabel.getValueAt(pilihBaris, 0);

            //hapus ditabel
            //Pilihan hapus = y/n
            int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin dihapus ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (konfirmasi == JOptionPane.YES_OPTION) {
                //hapus data sesuai NIM untuk dikirim ke JDBC
                q.hapus(nama);
            }
            loadtabel();
        }

    }//GEN-LAST:event_hapusActionPerformed

