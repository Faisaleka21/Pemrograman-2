    private void simpan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpan1ActionPerformed
        // TODO add your handling code here:

        String kntrk=kontrak1.getText();
        String NAMA=nama1.getText();
        String cr=cair1.getText();
        String tnr=tenor1.getSelectedItem().toString();
        String tlp=telepon1.getText();
        String mtr=motor1.getText();

        //Peringatan
        if(NAMA.isEmpty() || kntrk.isEmpty() || tlp.isEmpty() || cr.isEmpty() || mtr.isEmpty()){
            JOptionPane.showMessageDialog(this, "Harap Isi yang kosong","Error", JOptionPane.ERROR_MESSAGE);
            return;
        }   else{
            q.simpan(NAMA, kntrk, tlp, cr, tnr, mtr);
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!", "Success", JOptionPane.INFORMATION_MESSAGE);
        }

        //menampilkan
//        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
//        model.addRow(new Object[]{kntrk,NAMA,tlp,cr,tnr, mtr});

        kontrak.setText("");
        nama.setText("");
        cair.setText("");
        telepon.setText("");
        motor.setText("");
        tenor.setSelectedIndex(0);
        
        
        
        
    }//GEN-LAST:event_simpan1ActionPerformed

